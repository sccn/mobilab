     Bu(:,1).*Bv(:,1).*Bw(:,2) ,Bu(:,2).*Bv(:,1).*Bw(:,2) ,Bu(:,3).*Bv(:,1).*Bw(:,2) ,Bu(:,4).*Bv(:,1).*Bw(:,2), ...
