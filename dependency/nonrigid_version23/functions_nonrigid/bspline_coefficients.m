     Bu(:,1).*Bv(:,3).*Bw(:,2) ,Bu(:,2).*Bv(:,3).*Bw(:,2) ,Bu(:,3).*Bv(:,3).*Bw(:,2) ,Bu(:,4).*Bv(:,3).*Bw(:,2), ...
